a = "Eu, Ela"
b = "Tu"

concatenar = a+ "\n"+"e "  +b 
busca=(concatenar.find("T"))

print(concatenar)
print(concatenar[busca:])
print(concatenar.lower())
print(concatenar.upper())
print(concatenar.strip())
print(concatenar.split(" "))
print(concatenar.find("u"))

''''''
# lower é para deixar tudo minúsculo
# upper é para deixar tudo maiúsculo
# strip é pra deixar 
# split é pra deixar em lista 
# find é pra encontrar a posição
# busca é pra encontrar o restante do valor que procura 

